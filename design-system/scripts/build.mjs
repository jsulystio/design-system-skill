// build.mjs
// Reads tokens/figma.raw.json + inventory/components.json and generates:
//   tokens/tokens.json       DTCG-style tokens with light/dark values
//   tokens/variables.css     CSS custom properties (:root + dark overrides)
//   tokens/tailwind.theme.js Tailwind theme referencing the CSS vars
//   site/                    a static, self-updating guideline site
//
// The docs site is themed BY the tokens it documents, so the page you read
// is a live sample of the system. No dependencies, pure Node stdlib.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  read, write, exists, slug, cssVar, unitFor,
  resolveTokens, groupByCategory,
} from './lib.mjs';

const ROOT = path.resolve(fileURLToPath(import.meta.url), '../..');
const p = (...a) => path.join(ROOT, ...a);

const raw = read(p('tokens/figma.raw.json'));
const inventory = exists(p('inventory/components.json'))
  ? read(p('inventory/components.json'))
  : { components: [] };

const { flat, warnings } = resolveTokens(raw);
if (warnings.length) warnings.forEach((w) => console.warn('  warn:', w));

function cssValue(name, rec, mode) {
  const v = rec[mode];
  if (v === undefined || v === null) return null;
  if (rec.type === 'FLOAT') return `${v}${unitFor(name)}`;
  return String(v);
}

// ---- tokens.json (DTCG-ish, modes under $value) ----
const dtcg = {};
for (const [name, rec] of Object.entries(flat)) {
  const parts = name.split('/');
  let node = dtcg;
  for (let i = 0; i < parts.length - 1; i++) node = node[parts[i]] ||= {};
  node[parts[parts.length - 1]] = {
    $type: rec.type.toLowerCase(),
    $value: { light: rec.light, dark: rec.dark },
  };
}
write(p('tokens/tokens.json'), dtcg);

// ---- variables.css ----
{
  const light = [], dark = [];
  for (const [name, rec] of Object.entries(flat)) {
    const l = cssValue(name, rec, 'light');
    const d = cssValue(name, rec, 'dark');
    if (l != null) light.push(`  ${cssVar(name)}: ${l};`);
    if (d != null && d !== l) dark.push(`  ${cssVar(name)}: ${d};`);
  }
  const css =
`/* Generated by build.mjs. Do not edit by hand. Source of truth: Figma variables. */
:root {
${light.join('\n')}
}

:root[data-theme="dark"] {
${dark.join('\n')}
}

@media (prefers-color-scheme: dark) {
  :root:not([data-theme="light"]) {
${dark.join('\n')}
  }
}
`;
  write(p('tokens/variables.css'), css);
}

// ---- tailwind.theme.js ----
{
  const buckets = { colors: {}, spacing: {}, borderRadius: {}, fontSize: {}, fontWeight: {}, boxShadow: {} };
  const put = (bucket, key, name) => { buckets[bucket][key] = `var(${cssVar(name)})`; };
  for (const [name] of Object.entries(flat)) {
    const [head, ...rest] = name.split('/');
    const tail = rest.join('-');
    if (head === 'color') put('colors', tail, name);
    else if (head === 'space') put('spacing', tail, name);
    else if (head === 'radius') put('borderRadius', tail, name);
    else if (head === 'font' && rest[0] === 'size') put('fontSize', rest.slice(1).join('-'), name);
    else if (head === 'font' && rest[0] === 'weight') put('fontWeight', rest.slice(1).join('-'), name);
    else if (head === 'shadow') put('boxShadow', tail, name);
  }
  const js =
`// Generated by build.mjs. In tailwind.config.js:  theme: { extend: require('./tokens/tailwind.theme.js') }
module.exports = ${JSON.stringify(buckets, null, 2)};
`;
  write(p('tokens/tailwind.theme.js'), js);
}

// ---------- docs site ----------
const esc = (s) => String(s).replace(/[&<>"]/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' }[c]));
const groups = groupByCategory(flat);

function shell(title, body, depth = 0) {
  const up = '../'.repeat(depth);
  const nav = inventory.components
    .map((c) => `<a href="${up}components/${slug(c.name)}.html">${esc(c.name)}</a>`)
    .join('');
  return `<!doctype html>
<html lang="en" data-theme="light">
<head>
<meta charset="utf-8"><meta name="viewport" content="width=device-width, initial-scale=1">
<title>${esc(title)} &middot; ${esc(raw.meta?.name || 'Design system')}</title>
<link rel="stylesheet" href="${up}assets/tokens.css">
<link rel="stylesheet" href="${up}assets/site.css">
</head>
<body>
<header class="topbar">
  <a class="brand" href="${up}index.html">${esc(raw.meta?.name || 'Design system')}</a>
  <button id="theme" class="theme" aria-label="Toggle color theme">theme</button>
</header>
<div class="layout">
  <nav class="side" aria-label="Components">
    <a href="${up}index.html" class="side-lead">Foundations</a>
    <span class="side-label">Components</span>
    ${nav}
  </nav>
  <main class="content">${body}</main>
</div>
<script>
  const btn = document.getElementById('theme');
  const saved = localStorage.getItem('theme');
  if (saved) document.documentElement.setAttribute('data-theme', saved);
  btn.addEventListener('click', () => {
    const next = document.documentElement.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    document.documentElement.setAttribute('data-theme', next);
    localStorage.setItem('theme', next);
  });
</script>
</body>
</html>`;
}

function swatch(t) {
  return `<figure class="swatch">
    <div class="chip" style="background: var(${cssVar(t.name)})"></div>
    <figcaption><code>${esc(t.name)}</code><span>${esc(t.light ?? '')}</span></figcaption>
  </figure>`;
}
function scaleRow(t) {
  return `<tr><td><code>${esc(t.name)}</code></td><td class="mono">${esc(t.light ?? '')}</td></tr>`;
}

function foundationsPage() {
  const order = ['color', 'space', 'radius', 'font', 'shadow'];
  const cats = [...order.filter((c) => groups[c]), ...Object.keys(groups).filter((c) => !order.includes(c))];
  const sections = cats.map((cat) => {
    const items = groups[cat];
    if (cat === 'color') {
      return `<section id="${cat}"><h2>Color</h2><div class="swatches">${items.map(swatch).join('')}</div></section>`;
    }
    return `<section id="${cat}"><h2>${esc(cat[0].toUpperCase() + cat.slice(1))}</h2>
      <table class="scale"><tbody>${items.map(scaleRow).join('')}</tbody></table></section>`;
  }).join('');
  const compList = inventory.components.map((c) =>
    `<li><a href="components/${slug(c.name)}.html"><strong>${esc(c.name)}</strong><span>${esc(c.description || '')}</span></a></li>`
  ).join('');
  const body = `
    <div class="hero">
      <p class="eyebrow">Living guideline</p>
      <h1>${esc(raw.meta?.name || 'Design system')}</h1>
      <p class="lede">Generated from Figma variables. Everything on this page is rendered with the tokens it documents, so it stays true to the source. Last built ${new Date().toISOString().slice(0, 10)}.</p>
    </div>
    ${sections}
    <section id="components"><h2>Components</h2><ul class="complist">${compList}</ul></section>`;
  return shell('Foundations', body, 0);
}

function componentPage(c) {
  const props = (c.props || []).map((pr) =>
    `<tr><td><code>${esc(pr.name)}</code></td><td>${(pr.values || []).map((v) => `<span class="tag">${esc(v)}</span>`).join(' ')}</td></tr>`
  ).join('');
  const anatomy = (c.anatomy || []).map((a) => `<li>${esc(a)}</li>`).join('');
  const states = (c.states || []).map((s) => `<span class="tag">${esc(s)}</span>`).join(' ');
  const tokensUsed = (c.tokensUsed || []).map((t) =>
    `<li><span class="chip sm" style="background: var(${cssVar(t)})"></span><code>${esc(t)}</code></li>`
  ).join('');
  const dos = (c.usage?.do || []).map((d) => `<li>${esc(d)}</li>`).join('');
  const donts = (c.usage?.dont || []).map((d) => `<li>${esc(d)}</li>`).join('');
  const code = c.codeConnected
    ? `<p class="connected">Code-connected &rarr; <code>${esc(c.codePath || '')}</code></p>`
    : `<p class="unconnected">Not yet code-connected. Engineers should not hand-build this until a mapping exists.</p>`;
  const body = `
    <div class="crumb"><a href="../index.html">Foundations</a> / ${esc(c.name)}</div>
    <h1>${esc(c.name)}</h1>
    <p class="lede">${esc(c.description || '')}</p>
    ${code}
    <section><h2>Anatomy</h2><ul class="anatomy">${anatomy}</ul></section>
    <section><h2>Properties</h2><table class="scale"><tbody>${props}</tbody></table></section>
    <section><h2>States</h2><p class="tags">${states}</p></section>
    <section><h2>Tokens used</h2><ul class="tokenlist">${tokensUsed}</ul></section>
    <section class="usage">
      <div><h2>Do</h2><ul class="do">${dos}</ul></div>
      <div><h2>Don't</h2><ul class="dont">${donts}</ul></div>
    </section>`;
  return shell(c.name, body, 1);
}

